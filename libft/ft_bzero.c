#include "libft.h"

void	ft_bzero(void *ptr, size_t n)
{
	size_t			i;
	unsigned char	*str;

	i = 0;
	str = ptr;
	while (i < n)
	{
		str[i] = '\0';
		i++;
	}
	ptr = str;
}
