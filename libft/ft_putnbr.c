#include "libft.h"

void	ft_putnbr(int nb)
{
	if (nb > 0)
		nb *= -1;
	else if (nb == 0 || nb == -2147483648)
		nb *= -1;
	else
		ft_putchar('-');
	if (nb <= -10)
	{
		ft_putnbr(-nb / 10);
		nb = nb % 10;
	}
	ft_putchar(-nb + '0');
}
